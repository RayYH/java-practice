package com.rayyounghong.helper.container;

/**
 * A helper class which can stored two boolean values for test cases.
 *
 * @author ray
 */
public class BooleanContainer {

    public boolean boolOne;
    public boolean boolTwo;

    public BooleanContainer(boolean boolOne, boolean boolTwo) {
        this.boolOne = boolOne;
        this.boolTwo = boolTwo;
    }
}
