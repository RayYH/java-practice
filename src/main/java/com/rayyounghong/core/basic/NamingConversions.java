/*
 ***********************************************************************************************************************
 *
 * Classes and Interfaces:
 * 1. Class names and interface names should be nouns, in mixed case with the first letter of each internal word
 * capitalised;
 * 2. Use whole words and must avoid acronyms and abbreviations.
 *
 * Methods:
 * 1. Methods should be verbs, in mixed case with the first letter lowercase and with the first letter of each
 * internal word capitalised.
 *
 * Variables:
 * 1. Variable names should be short yet meaningful;
 * 2. Should not start with underscore(‘_’) or dollar sign ‘$’ characters.
 * 3. Should be mnemonic i.e, designed to indicate to the casual observer the intent of its use.
 * 4. One-character variable names should be avoided except for temporary variables.
 * 5. Common names for temporary variables are i, j, k, m, and n for integers; c, d, and e for characters.
 *
 * Constant variables:
 * 1. Should be all uppercase with words separated by underscores (“_”).
 * 2. There are various constants used in predefined classes like Float, Long, String etc.
 *
 * Packages:
 * 1. The prefix of a unique package name is always written in all-lowercase ASCII letters and should be one of the
 * top-level domain names, like com, edu, gov, mil, net, org.
 * 2. Subsequent components of the package name vary according to an organisation’s own internal naming conventions.
 *
 ***********************************************************************************************************************
 */
